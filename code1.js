// Sum numbers from 1 to 10
let sum = 0;
for (let i = 1; i <= 10; i++) {
    sum += i;
}

// Display the result
console.log("The sum of numbers from 1 to 20 is=", sum);

// Check if the sum is even or odd
if (sum % 2 === 0) {
    console.log("The sum is even.");
} else {
    console.log("The sum of numbers is odd.");
}